package org.hg.commons;

public class SixAxis {
    private int id;
    private double s;
    private double l;
    private double u;
    private double r;
    private double b;
    private double t;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getS() {
        return s;
    }

    public void setS(double s) {
        this.s = s;
    }

    public double getL() {
        return l;
    }

    public void setL(double l) {
        this.l = l;
    }

    public double getU() {
        return u;
    }

    public void setU(double u) {
        this.u = u;
    }

    public double getR() {
        return r;
    }

    public void setR(double r) {
        this.r = r;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }

    public double getT() {
        return t;
    }

    public void setT(double t) {
        this.t = t;
    }


}
